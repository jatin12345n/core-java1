package conditional_statements;

public class IFandELSE5 {
    public static void main(String[] args) {
        int a = 3;
        int b = -5;

        if (a > 0 || b > 0) {
            System.out.println("OR a and b are greater then zero");
        }
        else {
            System.out.println("OR a and b are not greater then zero");
        }

    }
}
